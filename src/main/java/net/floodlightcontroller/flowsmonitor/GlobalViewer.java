public class GlobalViewer {
	private HashMap<DatapathId,Collection<MatchField>> switches;
	
	public void init() {
		
	}

	public HashMap<DatapathId, Collection<MatchField>> getSwitches() {
		return switches;
	}

	public void setSwitches(HashMap<DatapathId, Collection<MatchField>> switches) {
		this.switches = switches;
	}
	
	public Collection<MatchField> getSwitch(DatapathId dpid) {
		return this.switches.get(dpid);
	}

	public void addSwitches(DatapathId dpid) {
		this.switches.put(dpid, new HashSet<MatchField>());
	}
	
	public void removeSwitches(DatapathId dpid) {
		this.switches.remove(dpid);
	}
}
